impl EaseFunction {
    /// Sample a value from this ease function.
    pub fn sample(&self, v: f32) -> f32 {
        use EaseFunction::*;
        match self {
            Linear => linear(v),
            QuadraticIn => quadratic_in(v),
            QuadraticOut => quadratic_out(v),
            QuadraticInOut => quadratic_in_out(v),
            CubicIn => cubic_in(v),
            CubicOut => cubic_out(v),
            CubicInOut => cubic_in_out(v),
            QuarticIn => quartic_in(v),
            QuarticOut => quartic_out(v),
            QuarticInOut => quartic_in_out(v),
            QuinticIn => quintic_in(v),
            QuinticOut => quintic_out(v),
            QuinticInOut => quintic_in_out(v),
            SineIn => sine_in(v),
            SineOut => sine_out(v),
            SineInOut => sine_in_out(v),
            CircularIn => circular_in(v),
            CircularOut => circular_out(v),
            CircularInOut => circular_in_out(v),
            ExponentialIn => exponential_in(v),
            ExponentialOut => exponential_out(v),
            ExponentialInOut => exponential_in_out(v),
            ElasticIn => elastic_in(v),
            ElasticOut => elastic_out(v),
            ElasticInOut => elastic_in_out(v),
            BackIn => back_in(v),
            BackOut => back_out(v),
            BackInOut => back_in_out(v),
            BounceIn => bounce_in(v),
            BounceOut => bounce_out(v),
            BounceInOut => bounce_in_out(v),
        }
    }
}

#[allow(clippy::type_complexity)]
pub fn ease_function_a_to_b_system<V>(
    f: impl Send + Sync + 'static + Fn(&V, &V, f32) -> V,
) -> impl Fn(
    Commands,
    Query<
        (Entity, &AToB<V, EaseFunction>, &TimeSpanProgress),
        Or<(Changed<EaseFunction>, Changed<TimeSpanProgress>)>,
    >,
    RemovedComponents<TimeSpanProgress>,
)
where
    V: Send + Sync + 'static,
{
    move |mut commands, query, mut removed| {
        query.iter().for_each(|(entity, a_to_b, progress)| {
            if progress.now_percentage.is_nan() {
                return;
            }
            let value = a_to_b.curve.sample(progress.now_percentage);

            commands
                .entity(entity)
                .insert(SetterValue(f(&a_to_b.a, &a_to_b.b, value)));
        });
        removed.read().for_each(|entity| {
            if let Some(mut entity) = commands.get_entity(entity) {
                entity.remove::<SetterValue<V>>();
            }
        });
    }
}

fn clamp(p: f32) -> f32 {
    match () {
        _ if p > 1.0 => 1.0,
        _ if p < 0.0 => 0.0,
        _ => p,
    }
}

fn linear(v: f32) -> f32 {
    clamp(v)
}

fn quadratic_in(v: f32) -> f32 {
    let p = clamp(v);
    p * p
}

fn quadratic_out(v: f32) -> f32 {
    let p = clamp(v);
    -(p * (p - 2.0))
}

fn quadratic_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        2.0 * p * p
    } else {
        (-2.0 * p * p) + (4.0 * p) - 1.0
    }
}

fn cubic_in(v: f32) -> f32 {
    let p = clamp(v);
    p * p * p
}

fn cubic_out(v: f32) -> f32 {
    let p = clamp(v);
    let f = p - 1.0;
    f * f * f + 1.0
}

fn cubic_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        4.0 * p * p * p
    } else {
        let f = (2.0 * p) - 2.0;
        0.5 * f * f * f + 1.0
    }
}

fn quartic_in(v: f32) -> f32 {
    let p = clamp(v);
    p * p * p * p
}

fn quartic_out(v: f32) -> f32 {
    let p = clamp(v);
    let f = p - 1.0;
    f * f * f * (1.0 - p) + 1.0
}

fn quartic_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        8.0 * p * p * p * p
    } else {
        let f = p - 1.0;
        -8.0 * f * f * f * f + 1.0
    }
}

fn quintic_in(v: f32) -> f32 {
    let p = clamp(v);
    p * p * p * p * p
}

fn quintic_out(v: f32) -> f32 {
    let p = clamp(v);
    let f = p - 1.0;
    f * f * f * f * f + 1.0
}

fn quintic_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        16.0 * p * p * p * p * p
    } else {
        let f = (2.0 * p) - 2.0;
        0.5 * f * f * f * f * f + 1.0
    }
}

fn sine_in(v: f32) -> f32 {
    let p = clamp(v);
    1. - (p * PI * 0.5).cos()
}

fn sine_out(v: f32) -> f32 {
    let p = clamp(v);
    (p * PI * 0.5).sin()
}

fn sine_in_out(v: f32) -> f32 {
    let p = clamp(v);
    -((p * PI).cos() - 1.) * 0.5
}

fn circular_in(v: f32) -> f32 {
    let p = clamp(v);
    1.0 - (1.0 - (p * p)).sqrt()
}

fn circular_out(v: f32) -> f32 {
    let p = clamp(v);
    ((2.0 - p) * p).sqrt()
}

fn circular_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        0.5 * (1.0 - (1.0 - 4.0 * (p * p)).sqrt())
    } else {
        0.5 * ((-((2.0 * p) - 3.0) * ((2.0 * p) - 1.0)).sqrt() + 1.0)
    }
}

fn exponential_in(v: f32) -> f32 {
    if v <= 0.0 {
        0.0
    } else {
        (2.0_f32).powf(10.0 * (v.min(1.0) - 1.0))
    }
}

fn exponential_out(v: f32) -> f32 {
    if v >= 1.0 {
        1.0
    } else {
        1.0 - (2.0_f32).powf(-10.0 * v.max(0.0))
    }
}

fn exponential_in_out(v: f32) -> f32 {
    if v <= 0.0 {
        return 0.0;
    }
    if v >= 1.0 {
        return 1.0;
    }

    if v < 0.5 {
        0.5 * (2.0_f32).powf((20.0 * v) - 10.0)
    } else {
        -0.5 * (2.0_f32).powf((-20.0 * v) + 10.0) + 1.0
    }
}

fn elastic_in(v: f32) -> f32 {
    let p = clamp(v);
    (13.0 * TAU * p).sin() * (2.0_f32).powf(10.0 * (p - 1.0))
}

fn elastic_out(v: f32) -> f32 {
    let p = clamp(v);
    (-13.0 * TAU * (p + 1.0)).sin() * (2.0_f32).powf(-10.0 * p) + 1.0
}

fn elastic_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        0.5 * (13.0 * TAU * (2.0 * p)).sin()
            * (2.0_f32).powf(10.0 * ((2.0 * p) - 1.0))
    } else {
        0.5 * ((-13.0 * TAU * ((2.0 * p - 1.0) + 1.0)).sin()
            * (2.0_f32).powf(-10.0 * (2.0 * p - 1.0))
            + 2.0)
    }
}

fn back_in(v: f32) -> f32 {
    let p = clamp(v);
    p * p * p - p * (p * PI).sin()
}

fn back_out(v: f32) -> f32 {
    let p = clamp(v);
    let f = 1.0 - p;
    1.0 - (f * f * f - f * (f * PI).sin())
}

fn back_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        let f = 2.0 * p;
        0.5 * (f * f * f - f * (f * PI).sin())
    } else {
        let f = 1.0 - (2.0 * p - 1.0);
        0.5 * (1.0 - (f * f * f - f * (f * PI).sin())) + 0.5
    }
}

fn bounce_in(v: f32) -> f32 {
    let p = clamp(v);
    1.0 - bounce_out(1.0 - p)
}

fn bounce_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 4.0 / 11.0 {
        (121.0 * p * p) / 16.0
    } else if p < 8.0 / 11.0 {
        (363.0 / 40.0 * p * p) - (99.0 / 10.0 * p) + 17.0 / 5.0
    } else if p < 9.0 / 10.0 {
        (4356.0 / 361.0 * p * p) - (35442.0 / 1805.0 * p) + 16061.0 / 1805.0
    } else {
        (54.0 / 5.0 * p * p) - (513.0 / 25.0 * p) + 268.0 / 25.0
    }
}

fn bounce_in_out(v: f32) -> f32 {
    let p = clamp(v);
    if p < 0.5 {
        0.5 * bounce_in(p * 2.0)
    } else {
        0.5 * bounce_out(p * 2.0 - 1.0) + 0.5
    }
}
